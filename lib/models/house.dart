class House{
  final String houseImagePath;
  final String houseName;
  final String roomType;
  final String houseRating;
  final String houseRent;
  final List<String> houseAmenities;
  final String houseLocation;
  final String university;

  House({
    required this.houseImagePath,
    required this.houseName,
    required this.roomType,
    required this.houseRating,
    required this.houseRent,
    required this.houseAmenities,
    required this.houseLocation,
    required this.university,
  });
}